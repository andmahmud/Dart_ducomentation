void main() {
  var marks = 60;

  switch (marks) {
    case 80:
      print("A+");
      break;
    case 70:
      print("A+");
      break;
    case 60:
      print("B");
      break;
    case 50:
      print("C");
      break;
    case 40:
      print("D");
      break;
    case 35:
      print("B");
      break;
    case 30:
      print("F");
      break;
    default:
      print("Invalid marks");
      break;
  }
}
